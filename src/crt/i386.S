.text
.global ccaprice_syscall_core
.global ccaprice_main
.global _start
.type   ccaprice_syscall_core,%function
.type   _start,%function

_start:
	xorl %ebp,%ebp
	popl %esi
	movl %esp,%ecx
	andl $0xfffffff0,%esp
	pushl %ecx
	pushl %esi
	call ccaprice_main
	hlt

#ifdef BSD
ccaprice_syscall_freebsd:
	int $0x80
	ret
#endif
	
ccaprice_syscall_core:
	pushl %ebp
	pushl %edi
	pushl %esi
	pushl %ebx
	
	#ifndef BSD
		movl 44(%esp),%ebp
		movl 40(%esp),%edi
		movl 36(%esp),%esi
		movl 32(%esp),%edx
		movl 28(%esp),%ecx
		movl 24(%esp),%ebx
		movl 20(%esp),%eax
		int  $0x80
	#else
		movl  20(%esp),%eax /* syscall id always EAX */
		pushl 24(%esp)
		pushl 28(%esp)
		pushl 32(%esp)
		pushl 36(%esp)
		pushl 40(%esp)
		pushl 44(%esp)
		call ccaprice_syscall_freebsd
		add %esp, 24 /* clean stack */
	#endif
	
	popl %ebx
	popl %esi
	popl %edi
	popl %ebp
	cmpl $-4095,%eax
	jae  ccaprice_syscall_error
	ret
	
.size ccaprice_syscall_core,.-ccaprice_syscall_core
.size _start,.-_start
	
